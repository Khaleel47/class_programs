class p5
{
    public static void main(String ar[])
    {
        int a=10,b=10,s,n;

        //incr
        s=++a;
        n=b++;
        System.out.println("s="+s+"a="+a+
        "\n n="+n+"b="+b);

        int x=20,y=20,p,q;
        //decr
        p=--x;
        q=y--;
        System.out.println("p="+p+"x="+x+
        "\n q="+q+"y="+y);
    }
}